export async function fetchRecommendVideos(
  count?: number,
): Promise<MyPageRecommendYoutubeResponse> {
  await new Promise((resolve) => setTimeout(resolve, 400));
  const videos =
    count !== undefined
      ? MOCK_RECOMMEND_VIDEOS.slice(0, count)
      : MOCK_RECOMMEND_VIDEOS;
  return {
    videos,
    isPersonalized: true,
    message: null,
  };
}
